    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       if(txtCodigoEvento.getText().isEmpty() || txtDescEvento.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Insira o evento Financeiro!");
           txtCodigoEvento.requestFocus();
       }else if(!!rbEntrada.isSelected() && !!rbSaida.isSelected()){
           JOptionPane.showMessageDialog(this, "Informe o Tipo de Movimentação!");
           rbEntrada.requestFocus();
           
       }else if (dtcData.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Informe a Data!");
            dtcData.requestFocus();
        }
       else if(txtCodCartao.getText().isEmpty() || txtDescCartao.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Insira o Cartão!!");
           txtCodCartao.requestFocus();
       }else{
           ModelConta.setIdEvento(Integer.parseInt(txtCodigoEvento.getText()));
           ModelConta.setIdcartao(Integer.parseInt(txtCodCartao.getText()));
           if(rbEntrada.isSelected()){
               ModelConta.setIdTipoMovimento(1);
           }else if(rbSaida.isSelected()){
               ModelConta.setIdTipoMovimento(2);
           }
           double valor = Double.parseDouble(txtValor.getText());           
           ModelConta.setData(formatador.formataDataBanco(dtcData.getDate()));
           if(ModelConta.getIdTipoMovimento() == 1){
               ModelConta.setValor(valor);
           }else if(ModelConta.getIdTipoMovimento() ==2){
               ModelConta.setValor(valor * -1);
           }
           
           ModelConta.setObservacao(txtObs.getText());
           
           int linhas = 0;
           if(contaControler.insereConta(ModelConta) <= 0){
               JOptionPane.showMessageDialog(this, "Erro ao Inserir Conta!");
               return;
           }else{
               JOptionPane.showConfirmDialog(this, "Salvo com Sucesso!");
           }
       }
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed


    private void txtCodCartaoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodCartaoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            List<ModelCartao> lista = new ArrayList<>();
            txtDescCartao.setText("");

            if (!txtCodCartao.getText().isEmpty()) {
                int idEvento = Integer.parseInt(txtCodCartao.getText());
                lista = cartao.buscaId(idEvento);
                if(lista.size() > 0 && lista.get(0).getIdSituacaoCadastro() > 0){
                    txtDescCartao.setText(lista.get(0).getDescricao());
                    btnSalvar.requestFocus(true);
                }else{
                    JOptionPane.showMessageDialog(this, "Nenhum Registro Encontrado!");

                }
            }else{
                JOptionPane.showMessageDialog(this, "Preencha Corretamente o Campo!");

            }

        }
    }//GEN-LAST:event_txtCodCartaoKeyPressed


    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        String descricao;
        int situacaoCad = cboSituacaoCadastro.getSelectedIndex();
        if(txtBusca.getText().isEmpty()){
            descricao = "%";
        }else{
            descricao = txtBusca.getText();
        }

        DefaultTableModel tabela = (DefaultTableModel) tbCartao.getModel();
        tabela.setNumRows(0);

        // int cont = listaCliente.size();
        // System.out.println(listaCliente.get(2).getNome());
        for (ModelCartao mc : controller.buscaCartao(descricao, situacaoCad)) {

            tabela.addRow(new Object[]{
                mc.getId(),
                mc.getDescricao(),
                mc.getDescTipo(),
                mc.getSaldo(),
                mc.getLimite(),
                mc.getVencimento()
            });
        }
    }//GEN-LAST:event_btnConsultarActionPerformed


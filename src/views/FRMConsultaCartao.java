    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
            int selecionada = tbCartao.getSelectedRow();
            if(selecionada <= -1){
                JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
                return;
            }else{
                ModelCartao model = new ModelCartao();
                model.setId(Integer.parseInt(tbCartao.getValueAt(selecionada, 0).toString()));
                model.setDescricao(tbCartao.getValueAt(selecionada, 1).toString());
                model.setDescTipo(tbCartao.getValueAt(selecionada, 2).toString());
                model.setSaldo(Double.parseDouble(tbCartao.getValueAt(selecionada, 3).toString()));
                model.setLimite(Double.parseDouble(tbCartao.getValueAt(selecionada, 4).toString()));
                model.setVencimento(Integer.parseInt(tbCartao.getValueAt(selecionada, 5).toString()));

                if(model.getDescTipo().equals("DEBITO")){
                    model.setIdTipocartao(1);
                } else if(model.getDescTipo().equals("CREDITO")){
                    model.setIdTipocartao(2);
                }else{
                    model.setIdTipocartao(3);
                }
                
                FRMCadastroCartao cadastroCartao = new FRMCadastroCartao(model);
                getParent().add(cadastroCartao);
                cadastroCartao.setVisible(true);
                

            }

        

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selecionada = tbCartao.getSelectedRow();
        int idSelecionado  = 0;
        if(selecionada <= -1){
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
            return;
        }else{
            idSelecionado = (Integer.parseInt(tbCartao.getValueAt(selecionada, 0).toString()));
            int linhas = controller.apagaCartao(idSelecionado);
            if(linhas > 0 ){
                JOptionPane.showMessageDialog(this, "Excluido com sucesso!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


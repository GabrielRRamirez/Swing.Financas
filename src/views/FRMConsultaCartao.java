    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed
        try {
            int selecionada = tbCartao.getSelectedRow();
            if (selecionada <= -1) {
                JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
                return;
            } else {
                cartao.setId(Integer.parseInt(tbCartao.getValueAt(selecionada, 0).toString()));
                cartao.setDescricao(tbCartao.getValueAt(selecionada, 1).toString());
                FRMAdicionaMovimento.getInstancia().carregaCartao(cartao);

                System.out.println(Integer.toString(cartao.getId()) + "  " + cartao.getDescricao());
                this.dispose();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro:\n" + e);
        }
    }//GEN-LAST:event_btnCarregarActionPerformed



    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed
        try {
            int selecionada = tbEvento.getSelectedRow();
            if (selecionada <= -1) {
                JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
                return;
            } else {
                evento.setId(Integer.parseInt(tbEvento.getValueAt(selecionada, 0).toString()));
                evento.setDescricao(tbEvento.getValueAt(selecionada, 1).toString());
                FRMAdicionaMovimento.getInstancia().carregaEvento(evento);
                
                System.out.println(Integer.toString(evento.getId()) + "  " + evento.getDescricao());
                this.dispose();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro:\n" + e);
        }
    }//GEN-LAST:event_btnCarregarActionPerformed



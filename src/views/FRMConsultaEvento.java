    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        String descricao;
        int situacaoCad = cboSituacaoCadastro.getSelectedIndex();
        if (txtBusca.getText().isEmpty()) {
            descricao = "%";
        } else {
            descricao = txtBusca.getText();
        }

        DefaultTableModel tabela = (DefaultTableModel) tbEvento.getModel();
        tabela.setNumRows(0);

        for (ModelEvento mc : controller.buscaEvento(descricao, situacaoCad)) {

            tabela.addRow(new Object[]{
                mc.getId(),
                mc.getDescricao(),});
        }
    }//GEN-LAST:event_btnConsultarActionPerformed


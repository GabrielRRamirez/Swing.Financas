    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            int selecionada = tbEvento.getSelectedRow();
            if (selecionada <= -1) {
                JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
                return;
            } else {
                ModelEvento model = new ModelEvento();
                model.setId(Integer.parseInt(tbEvento.getValueAt(selecionada, 0).toString()));
                model.setDescricao(tbEvento.getValueAt(selecionada, 1).toString());

                FRMCadastroEvento cadastroEvento = new FRMCadastroEvento(model);
                getParent().add(cadastroEvento);
                cadastroEvento.setVisible(true);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro:\n" + e);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selecionada = tbEvento.getSelectedRow();
        int idSelecionado = 0;
        if (selecionada <= -1) {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado!");
            return;
        } else {
            idSelecionado = (Integer.parseInt(tbEvento.getValueAt(selecionada, 0).toString()));
            int linhas = controller.apagaEvento(idSelecionado);
            if (linhas > 0) {
                JOptionPane.showMessageDialog(this, "Excluido com sucesso!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

